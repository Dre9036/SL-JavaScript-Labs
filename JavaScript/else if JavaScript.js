/* else if JavaScript */

/* You can use the else if statement to specify a new condition if the
 first condition is false.

Example: */

var course = 1;
if (course == 1) {
   document.write("<h1>HTML Tutorial</h1>");
} else if (course == 2) {
   document.write("<h1>CSS Tutorial</h1>");
} else {
   document.write("<h1>JavaScript Tutorial</h1>");
}


