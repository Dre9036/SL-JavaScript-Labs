// Continue JAVASCRIPT

/* The continue statement breaks only one iteration 
in the loop, and continues with the next iteration. */

for (i = 0; i <= 10; i++) {
   if (i == 5) {
      continue; 
   }
   document.write(i + "<br />");
}

//IMAGE 

// The value 5 is not printed, because continue skips that iteration of the loop.