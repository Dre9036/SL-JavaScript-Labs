/* You can access object properties in two ways.

STNYAX:

objectName.propertyName
//or
objectName['propertyName']

*/ 

// This example demonstrates how to access the age of our person object.

var person = {
 name: "John", age: 31, 
 favColor: "green", height: 183
};
var x = person.age;
var y = person['age'];